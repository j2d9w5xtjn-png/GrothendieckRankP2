#!/usr/bin/env python3
r"""Stratified S' search on two ramified nonprincipal length-five bases.

The division syzygy is computed exhaustively.  Socle shifts are discarded
exactly (fiber2 puts phi in m), and an F_2-basis of the residual syzygy
quotient is derived rather than hard-coded.  Failure at each augmentation
basis vector is then an existential/ForAll formula over only the residual
bits.  Every parametrization is checked against the complete finite syzygy
set before any Hopf query runs.
"""

